	public List<Adiacenza> getStatiConVoli(){
		String sql = "SELECT DISTINCT a1.STATE as s1, a2.STATE as s2 " + 
					 "FROM airports a1, airports a2, flights f " + 
					 "WHERE a1.ID>a2.ID AND a1.STATE<>a2.STATE " + 
					 "		AND ((f.ORIGIN_AIRPORT_ID=a1.ID AND f.DESTINATION_AIRPORT_ID=a2.ID) OR (f.ORIGIN_AIRPORT_ID=a2.ID AND f.DESTINATION_AIRPORT_ID=a2.ID))";
		
		List<Adiacenza> adiacenze = new ArrayList<>();
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet rs = st.executeQuery();

			while (rs.next()) {
				Adiacenza a = new Adiacenza(rs.getString("s1"), rs.getString("s2"), 0);
				adiacenze.add(a);
			}

			conn.close();
			return adiacenze;
		} catch (SQLException e) {
			e.printStackTrace();
			System.out.println("Errore connessione al database");
			throw new RuntimeException("Error Connection Database");
		}
	}
	
	public Integer getPesoArco(String s1, String s2) {
		String sql = "SELECT COUNT(DISTINCT f.TAIL_NUMBER) as cnt " + 
				"FROM airports a1, airports a2, flights f " + 
				"WHERE a1.ID<>a2.ID AND a1.STATE=? AND a2.STATE=? AND f.ORIGIN_AIRPORT_ID=a1.ID AND f.DESTINATION_AIRPORT_ID=a2.ID";
		
		Integer peso = 0;
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, s1);
			st.setString(2, s2);
			ResultSet rs = st.executeQuery();

			if(rs.next()) {
				peso = rs.getInt("cnt");
			}

			conn.close();
			return peso;
		} catch (SQLException e) {
			e.printStackTrace();
			System.out.println("Errore connessione al database");
			throw new RuntimeException("Error Connection Database");
		}
		
	}
